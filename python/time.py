# import the time module
import time

time.sleep(1)  # This will pause the program for 1 second

# This will print "Hello World!" to the screen after 1 second
print("Hello World!")

time.sleep(10)  # This will pause the program for 10 seconds

# This will print "Goodbye World!" to the screen after 10 seconds after the first print statement
print("Goodbye World!")
