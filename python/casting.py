# In python you can cast variables to different types.

# String to integer

a = "1"  # This is a string
b = int(a)  # This is an integer

# String to float

a = "1.0"  # This is a string
b = float(a)  # This is a float

# String to boolean

a = "True"  # This is a string
b = bool(a)  # This is a boolean

# String to list

a = "Hello World!"  # This is a string
b = list(a)  # This is a list
