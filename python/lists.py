x = [1, 2, 3, 4, 5]  # Create a list

print(x[0])  # Print the first item in the list
print(x[1])  # Print the second item in the list
print(x[2])  # Print the third item in the list
print(x[3])  # Print the fourth item in the list
print(x[4])  # Print the fifth item in the list

y = ["Hello", "World", "!"]  # Create a list

print(y[0])  # Print the first item in the list
print(y[1])  # Print the second item in the list
print(y[2])  # Print the third item in the list

# List operations
print(x.count(1))  # Count the number of times 1 appears in the list
print(x.index(1))  # Find the index of the first 1 in the list
print(x.pop(0))  # Remove the first item in the list
print(x.pop(1))  # Remove the second item in the list
print(x.pop(2))  # Remove the third item in the list
print(x)
